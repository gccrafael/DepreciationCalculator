    private void jbtnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCalcActionPerformed
        double cost, salvage;
        int life;
        statusMessageLabel.setText("");
        try 
        {
            cost = Double.parseDouble(jtxtCost.getText());
            salvage = Double.parseDouble(jtxtSalvage.getText());
            life = Integer.parseInt(jtxtLife.getText());
        }
        catch (Exception e)
        {
            statusMessageLabel.setText("Illegal input" + e.getMessage());
            jtxtCost.requestFocusInWindow();
            return;
        }       
        asset = new Asset(jtxtName.getText(), cost, salvage, life);
        if (!asset.getErrorMsg().isEmpty())
        {
                statusMessageLabel.setText(asset.getErrorMsg());
                return;
        }
        NumberFormat curr = NumberFormat.getCurrencyInstance();
        String[] cols = {"Year", "Beginning Balance", "Annual Depreciation"
                        , "Ending Balance"};
        String [][] t = new String[asset.getLife()][4];
        DefaultTableModel m = new DefaultTableModel(t, cols);
        jTable1.setModel(m);
        DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();
        rightRenderer.setHorizontalAlignment( JLabel.RIGHT );
        jTable1.getColumnModel().getColumn(0).setCellRenderer( rightRenderer );
        jTable1.getColumnModel().getColumn(1).setCellRenderer( rightRenderer );
        jTable1.getColumnModel().getColumn(2).setCellRenderer( rightRenderer );
        jTable1.getColumnModel().getColumn(3).setCellRenderer( rightRenderer );
                
        for (int i = 0; i < asset.getLife(); i++)
        {
            jTable1.setValueAt(i+1, i, 0);
            if (jradStraight.isSelected())
            {               
                jTable1.setValueAt(curr.format(asset.getAnnDep()), i, 2);
                jTable1.setValueAt(curr.format(asset.getBegBal(i, 0)), i, 1);
                jTable1.setValueAt(curr.format(asset.getEndBal(i, 0)), i, 3);
            }    
            else
            {
                jTable1.setValueAt(curr.format(asset.getAnnDep(i)), i, 2);
                jTable1.setValueAt(curr.format(asset.getBegBal(i, 1)), i, 1);
                jTable1.setValueAt(curr.format(asset.getEndBal(i, 1)), i, 3);
            }    
            
        }    
    }//GEN-LAST:event_jbtnCalcActionPerformed

